#include <stdio.h>

int main() {
    // ประกาศอาเรย์เก็บข้อมูลผู้ใช้ 3 คน
    char name[3];        // ชื่อ 1 ตัวอักษร (A, B, C, ...)
    int age[3];          // อายุ (จำนวนเต็ม)
    float height[3];     // ส่วนสูง (เซนติเมตร ทศนิยม 1 ตำแหน่ง)
    float weight[3];     // น้ำหนัก (กิโลกรัม ทศนิยม 1 ตำแหน่ง)
    char grade[3];       // รหัสเกรด (ตัวอักษร 1 ตัว เช่น A, B, C)

    // รับข้อมูล 3 คน 1 คน ต่อ 1 บรรทัด
    // รูปแบบข้อมูลที่รับ: ชื่อ อายุ ส่วนสูง น้ำหนัก รหัสเกรด
    // ใช้ scanf() รับค่าแบบต่อเนื่อง โดยมีช่องว่างเป็นตัวแบ่งข้อมูล
    for (int i = 0; i < 3; i++) {
        scanf(" %c %d %f %f %c", &name[i], &age[i], &height[i], &weight[i], &grade[i]);
    }

    // แสดงผลข้อมูลในรูปแบบตาราง
    // ใช้ printf() พร้อม format specifier เพื่อจัดตำแหน่งข้อมูลให้สวยงาม
    printf("+------+-----+-----------+-----------+------------+\n");
    printf("| Name | Age | Height(cm)| Weight(kg)| Grade Code |\n");
    printf("+------+-----+-----------+-----------+------------+\n");

    for (int i = 0; i < 3; i++) {
        printf("|  %c   | %3d |   %6.1f  |   %6.1f  |     %c      |\n",
            name[i], age[i], height[i], weight[i], grade[i]);
    }

    printf("+------+-----+-----------+-----------+------------+\n");

    return 0;  // จบโปรแกรม
}
