#include <stdio.h>

int main() {
    // ประกาศอาร์เรย์เก็บข้อมูลของผู้ใช้ 3 คน
    char name[3];        // ชื่อ (ตัวอักษรเดียว เช่น A, B, C)
    int age[3];          // อายุ (จำนวนเต็ม)
    float height[3];     // ส่วนสูง (เซนติเมตร)
    float weight[3];     // น้ำหนัก (กิโลกรัม)
    char grade[3];       // เกรด (ตัวอักษร เช่น A, B, C)

    // รับข้อมูลจากผู้ใช้: 3 คน โดยใช้ loop
    // รูปแบบ: <ตัวอักษร> <อายุ> <ส่วนสูง> <น้ำหนัก> <เกรด>
    for (int i = 0; i < 3; i++) {
        // ช่องว่างหน้า %c แรกสำคัญมาก: เพื่อกิน newline ที่อาจค้างอยู่
        scanf(" %c %d %f %f %c", &name[i], &age[i], &height[i], &weight[i], &grade[i]);
    }

    // แสดงหัวตาราง
    printf("+------+-----+-----------+-----------+------------+\n");
    printf("| Name | Age | Height(cm)| Weight(kg)| Grade Code |\n");
    printf("+------+-----+-----------+-----------+------------+\n");

    // แสดงข้อมูลแต่ละคนในรูปแบบตาราง
    for (int i = 0; i < 3; i++) {
        printf("|  %c   | %3d |   %6.1f  |   %6.1f  |     %c      |\n",
            name[i], age[i], height[i], weight[i], grade[i]);
    }

    // ปิดท้ายด้วยเส้นล่าง
    printf("+------+-----+-----------+-----------+------------+\n");

    return 0;
}
